package exemplo1;

public class Roupa {
    public String descricao;
    public String cor;
    public String modelo;
    public String tamanho;
    public String colecao;

    public Roupa() {
    }

    public Roupa(String descricao, String cor, String modelo, String tamanho, String colecao) {
        this.descricao = descricao;
        this.cor = cor;
        this.modelo = modelo;
        this.tamanho = tamanho;
        this.colecao = colecao;
    }
}
